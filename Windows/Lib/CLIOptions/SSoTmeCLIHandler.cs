        /// <summary>
        /// Invokes a tool via the transpiler mechanism and returns the content of a specific output file.
        /// Used by MyEffortlessAPIService to call cli-cloud-bridge for auth operations.
        /// </summary>
        public static string InvokeToolAndGetOutput(string commandLine, string outputFileName)
        {
            var tempDir = Path.Combine(Path.GetTempPath(), $"ssotme_auth_{Guid.NewGuid():N}");
            Directory.CreateDirectory(tempDir);
            var savedDir = Environment.CurrentDirectory;

            try
            {
                Environment.CurrentDirectory = tempDir;

                // Write minimal ssotme.json so the handler has a valid project
                var ssotmeJsonPath = Path.Combine(tempDir, "ssotme.json");
                File.WriteAllText(ssotmeJsonPath, $@"{{
  ""ShowHidden"": false,
  ""ShowAllFiles"": false,
  ""CurrentPath"": null,
  ""SSoTmeProjectFiles"": null,
  ""Name"": ""auth_temp"",
  ""ProjectSettings"": [
    {{
      ""ProjectSettingId"": ""{Guid.NewGuid()}"",
      ""Name"": ""project-name"",
      ""Value"": ""auth_temp""
    }}
  ],
  ""ProjectTranspilers"": []
}}");

                // Ensure the tool URL is in the global tool_urls.json
                // (it should already be there from RunRemoteToolsRefresh, but ensure as fallback)
                var globalToolUrlsPath = Path.Combine(SSOTMEKey.SSoTmeDir.FullName, "tool_urls.json");
                Dictionary<string, string> urlMappings;
                if (File.Exists(globalToolUrlsPath))
                {
                    urlMappings = JsonConvert.DeserializeObject<Dictionary<string, string>>(File.ReadAllText(globalToolUrlsPath)) ?? new Dictionary<string, string>();
                }
                else
                {
                    urlMappings = new Dictionary<string, string>();
                }
                if (!urlMappings.ContainsKey(TRANSPILERS_LISTER_TOOL_NAME))
                {
                    urlMappings[TRANSPILERS_LISTER_TOOL_NAME] = LATEST_TRANSPILERS_LISTER_URL;
                    File.WriteAllText(globalToolUrlsPath, JsonConvert.SerializeObject(urlMappings, Formatting.Indented));
                }

                var handler = new SSoTmeCLIHandler();
                handler.skipRemoteToolsLookup = true;
                handler.suppressVersionLabel = true;
                handler.suppressTranspilerErrorOutput = true;
                handler.commandLine = commandLine;
                handler.ParseCommand();
                CliLog.SuppressFileLog = true;
                try { handler.TranspileProject(); }
                catch { /* best-effort — caller treats null as "skip quota / skip project registration" */ }
                finally { CliLog.SuppressFileLog = false; }

                // Read the output file
                var outputPath = Path.Combine(tempDir, outputFileName);
                if (File.Exists(outputPath))
                    return File.ReadAllText(outputPath);

                return null;
            }
            finally
            {
                Environment.CurrentDirectory = savedDir;
                try { Directory.Delete(tempDir, true); } catch { }
            }
        }

        private void RefreshRemoteTools()
        {
            var remoteToolsDir = GetRemoteToolsDir();
            var toolsJsonPath = Path.Combine(remoteToolsDir, "ssotme-tools.json");
            var cliVersionPath = Path.Combine(remoteToolsDir, "cli_version");
            var cachedCliVersion = File.Exists(cliVersionPath) ? File.ReadAllText(cliVersionPath).Trim() : null;

            if (File.Exists(toolsJsonPath))
            {
                File.Delete(toolsJsonPath);
                if (this.debug) Console.WriteLine($"DEBUG: deleted {toolsJsonPath}");
            }
            if (File.Exists(cliVersionPath))
            {
                File.Delete(cliVersionPath);
                if (this.debug) Console.WriteLine($"DEBUG: deleted {cliVersionPath}");
            }
            // Also reset the bridge version index so -refreshTools picks up the
            // current latestBridgeVersion from Airtable unconditionally, not only
            // when versionIndex has advanced past the locally-cached value.
            var bridgeIdxPath = Path.Combine(SSOTMEKey.SSoTmeDir.FullName, "bridge_version_index");
            if (File.Exists(bridgeIdxPath))
            {
                File.Delete(bridgeIdxPath);
                if (this.debug) Console.WriteLine($"DEBUG: deleted {bridgeIdxPath}");
            }

            _hasRunRemoteToolsUpdate = false;

            CliLog.LogLine("Refreshing remote tools index...");
            EnsureRemoteToolsInitialized();
            RunRemoteToolsRefresh(remoteToolsDir, cliVersionPath, cachedCliVersion);
            CliLog.LogLine("Remote tools index refreshed.");
        }

        private void EnsureRemoteToolsInitialized()
        {
            var remoteToolsDir = GetRemoteToolsDir();
            var toolsJsonPath = Path.Combine(remoteToolsDir, "ssotme-tools.json");
            if (File.Exists(toolsJsonPath))
            {
                if (this.debug) Console.WriteLine($"DEBUG: remote_tools ssotme-tools.json found at {toolsJsonPath}");
                return;
            }

            if (this.debug) Console.WriteLine($"DEBUG: ssotme-tools.json not found, bootstrapping remote_tools at {remoteToolsDir}");
            if (!Directory.Exists(remoteToolsDir))
            {
                Directory.CreateDirectory(remoteToolsDir);
                if (this.debug) Console.WriteLine($"DEBUG: Created remote_tools directory: {remoteToolsDir}");
            }

            // Write placeholder first — prevents any re-entrant call from re-bootstrapping
            File.WriteAllText(toolsJsonPath, "{\"transpilers\":{}}");
            CliLog.LogLine("Initializing CLI tool URL index...");

            // Write tool_urls.json with the effective transpilers URL
            var effectiveUrl = TryGetUrlFromFileUrls(SSoTmeCLIHandler.TRANSPILERS_LISTER_TOOL_NAME) ?? SSoTmeCLIHandler.LATEST_TRANSPILERS_LISTER_URL;
