            if (useRabbitMQ)
            {
                this.AccountHolder.Init("cli@aicapture.io", "e8f398c3c1854bf7b9b0deb123ba0127");
            }
            this.ProxyRequest();

            var waitForCook = Task.Factory.StartNew(() =>
            {
                if (!useRabbitMQ)
                {
                    var dots = new[] { "   ", ".  ", ".. ", "..." };
                    int elapsed = 0;
                    int dotIndex = 0;
                    while (ReferenceEquals(result, null))
                    {
                        Thread.Sleep(500);
                        elapsed += 500;
                        if (elapsed >= 15000)
                        {
                            if (!_showedBootMessage)
                            {
                                var bootMessage = $"Waiting for the remote transpiler '{this.transpiler}' to boot up    ";
                                Console.Write(bootMessage);
                                _bootMessageLength = bootMessage.Length;
                                _showedBootMessage = true;
                            }
                            Console.Write("\b\b\b" + dots[dotIndex % 4]);
                            dotIndex++;
                        }
                    }
                    ClearBootLine();
                }
                else
                {
                    while (ReferenceEquals(result, null)) Thread.Sleep(100);
                }
            });

            waitForCook.Wait(this.waitTimeout);

            if (ReferenceEquals(result, null))
            {
                result = AccountHolder.CreatePayload();
                result.Exception = new TimeoutException("Timed out waiting for cook");
            }
            result.SSoTmeProject = this.AICaptureProject;
        }

