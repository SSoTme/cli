                        }
                        else
                        {
                            ShowError("This is likely an issue with the transpiler, not with SSoTme.", ConsoleColor.Yellow);
                        }

                        ShowError("The transpiler may have received invalid input or encountered an internal error.", ConsoleColor.Yellow);
                        ShowError("=======================================================\n", ConsoleColor.Yellow);

                        var nextException = result.Exception;
                        while (!(nextException is null))
                        {
                            ShowError("ERROR: " + nextException.Message);
                            if (!string.IsNullOrEmpty(nextException.StackTrace))
                                ShowError(nextException.StackTrace);
                            nextException = nextException.InnerException;
                        };

                        // Provide diagnostic hints for common errors
                        var errMsg = result.Exception?.Message ?? "";
                        if (errMsg.Contains("SSL connection could not be established"))
                        {
                            ShowError("\nHINT: The transpiler could not establish a secure connection to an external API.", ConsoleColor.Cyan);
                            ShowError("  Possible causes:", ConsoleColor.Cyan);
                            ShowError("  - The remote API may be temporarily down or unreachable", ConsoleColor.Cyan);
                            ShowError("  - Your network may be blocking the connection (firewall, proxy, VPN)", ConsoleColor.Cyan);
                            ShowError("  - The remote API's SSL certificate may have expired", ConsoleColor.Cyan);
                            ShowError("  Try again in a few minutes, or check your network connectivity.\n", ConsoleColor.Cyan);
                        }

                        return -1;
                    }
                    else
                    {
                        var finalResult = 0;

                        if (!ReferenceEquals(result.Transpiler, null))
                        {
                            //Console.WriteLine("\n\nTRANSPILER MATCHED: {0}\n\n", result.Transpiler.Name);
                        }

                        if (this.clean) result.CleanFileSet(deleteEmptyDirs: false);
                        else
                        {
                            finalResult = result.SaveFileSet(this.skipClean);
                            updateProject = true;
                        }
                        return finalResult;

                    }
                }

                return 0;
            }
            finally
            {
                if (!ReferenceEquals(AccountHolder, null)) AccountHolder.Disconnect();
                if (updateProject)
                {
                    if (isBuildOperation && !ReferenceEquals(projectTranspiler, null))
                    {
                        // Update runtime-only properties without saving during build operations
                        GetProjectOrThrow().UpdateRuntimeOnly(projectTranspiler, result);

                        // Track what version and URL were used (soft pin — auto-updates each run)
                        if (!String.IsNullOrEmpty(this.ResolvedVersionKey))
                        {
                            bool changed = false;
                            if (!String.Equals(projectTranspiler.LastVersionUsed, this.ResolvedVersionKey))
                            {
                                projectTranspiler.LastVersionUsed = this.ResolvedVersionKey;
                                changed = true;
                            }
                            if (!String.IsNullOrEmpty(this.ResolvedVersionUrl) && !String.Equals(projectTranspiler.LastUrl, this.ResolvedVersionUrl))
                            {
                                projectTranspiler.LastUrl = this.ResolvedVersionUrl;
                                changed = true;
                            }
                            if (changed) GetProjectOrThrow().Save();
                        }
                    }
                    else
                    {
                        // Full update with save for non-build operations
                        if (this.install) this.AICaptureProject.Install(result, this.transpilerGroup, this.dryRun, this.ResolvedVersionKey);
                        else if (!ReferenceEquals(projectTranspiler, null))
                        {
                            GetProjectOrThrow().Update(projectTranspiler, result);
                        }

                        // Track what version and URL were used (soft pin — auto-updates each run)
                        if (!this.install && !ReferenceEquals(projectTranspiler, null) && !String.IsNullOrEmpty(this.ResolvedVersionKey))
                        {
                            bool changed = false;
                            if (!String.Equals(projectTranspiler.LastVersionUsed, this.ResolvedVersionKey))
                            {
                                projectTranspiler.LastVersionUsed = this.ResolvedVersionKey;
                                changed = true;
                            }
                            if (!String.IsNullOrEmpty(this.ResolvedVersionUrl) && !String.Equals(projectTranspiler.LastUrl, this.ResolvedVersionUrl))
                            {
                                projectTranspiler.LastUrl = this.ResolvedVersionUrl;
                                changed = true;
                            }
                            if (changed) GetProjectOrThrow().Save();
                        }

                        // -latest: update LastVersionUsed to the resolved head version (clears any hard pin)
                        if (this.latest && !String.IsNullOrEmpty(this.ResolvedVersionKey) && !ReferenceEquals(projectTranspiler, null))
                        {
                            projectTranspiler.LastVersionUsed = this.ResolvedVersionKey;
                            if (!String.IsNullOrEmpty(this.ResolvedVersionUrl))
                                projectTranspiler.LastUrl = this.ResolvedVersionUrl;
                            // -latest clears hard pin so future runs auto-track HEAD again
                            if (!String.IsNullOrEmpty(projectTranspiler.PinnedVersion))
                            {
                                CliLog.LogLine($"Cleared hard pin on {this.ResolvedToolName ?? this.transpiler} (was {projectTranspiler.PinnedVersion})");
                                projectTranspiler.PinnedVersion = null;
                            }
                            GetProjectOrThrow().Save();
                            CliLog.LogLine($"Now using {this.ResolvedToolName ?? this.transpiler} {this.ResolvedVersionKey} [latest]");
                        }
                    }
                }

                // Clean up empty directories after tool run completes
                if (!ReferenceEquals(this.AICaptureProject, null))
                {
                    this.AICaptureProject.CleanEmptyDirectories(Environment.CurrentDirectory);
                }
            }
        }

        private void InitiateCloneSeedingProcess()
        {
            var requestedSeedName = this.RemainingArguments.FirstOrDefault();
            var seed = this.ListSeeds(true, requestedSeedName);
            if (seed is null) return;
            string seedRootPath = default;
            var task = Task.Run(async () => seedRootPath = await this.CloneSeed(seed));
            task.Wait(300000);
