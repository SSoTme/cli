        // Parse a version key like "v2026.03.13.1534" into a comparable tuple.
        // From a list of fully-qualified tool names (account/package/tool), pick the best:
        // prefer the one whose account segment is "effortless", otherwise alphabetical first.
        private string SelectBestCandidate(List<string> candidates)
        {
            var effortlessMatch = candidates
                .Where(c => c.IndexOf('/') >= 0 &&
                            c.Substring(0, c.IndexOf('/')).Equals("effortless", StringComparison.OrdinalIgnoreCase))
                .OrderBy(c => c, StringComparer.OrdinalIgnoreCase)
                .FirstOrDefault();

            if (effortlessMatch != null) return effortlessMatch;

            return candidates.OrderBy(c => c, StringComparer.OrdinalIgnoreCase).First();
        }

        // Accepts both the ExtractVersionFromUrl format (v2026.04.03.1718 — 4 dotted parts, time is HHMM)
        // and the package.json / CLI_VERSION format (2026-04-19.23.12 — date uses hyphens, time is split HH.MM).
        // Returns (year, month, day, timeAsHHMM) for comparable ordering.
        private (int, int, int, int) ParseVersionKey(string key, bool debug)
        {
            try
            {
                var s = key.TrimStart('v').Replace('-', '.');
                var parts = s.Split('.');
                if (parts.Length == 4 &&
                    int.TryParse(parts[0], out int y) &&
                    int.TryParse(parts[1], out int mo) &&
                    int.TryParse(parts[2], out int d) &&
                    int.TryParse(parts[3], out int t))
                {
                    return (y, mo, d, t);
                }
                if (parts.Length == 5 &&
                    int.TryParse(parts[0], out int y5) &&
                    int.TryParse(parts[1], out int mo5) &&
                    int.TryParse(parts[2], out int d5) &&
                    int.TryParse(parts[3], out int h5) &&
                    int.TryParse(parts[4], out int m5))
                {
                    return (y5, mo5, d5, h5 * 100 + m5);
                }
            }
            catch {
                if (debug) {
                    Console.ForegroundColor = ConsoleColor.Yellow;
                    Console.WriteLine($"Could not parse version: {key}");
                    Console.ResetColor();
                }
            }
            return (0, 0, 0, 0);
        }

        private string ExtractVersionFromUrl(string url)
        {
            try
            {
                // Regex pattern: -v(\d{4}-\d{2}-\d{2}-\d{4})
                var regex = new Regex(@"-v(\d{4}-\d{2}-\d{2}-\d{4})");
                var match = regex.Match(url);

                if (match.Success)
                {
                    // Extract: 2025-12-22-1522
                    var versionPart = match.Groups[1].Value;

                    // Split and format: v2025.12.22.1522
                    var parts = versionPart.Split('-');
                    if (parts.Length == 4)
                    {
                        return $"v{parts[0]}.{parts[1]}.{parts[2]}.{parts[3]}";
                    }
                }
            }
            catch (Exception ex)
            {
                Console.ForegroundColor = ConsoleColor.Yellow;
                Console.WriteLine($"  Warning: Could not extract version from URL: {ex.Message}");
                Console.ForegroundColor = ConsoleColor.Gray;
            }

            return null;
        }

        private string GetVersionFromBridgeServer(string toolName, string url)
        {
            try
            {
                var apiUrl = $"{this.BridgeServerBaseUrl}/api/transpilers/version?name={Uri.EscapeDataString(toolName)}&url={Uri.EscapeDataString(url)}";

                using (var client = new HttpClient())
                {
                    client.Timeout = TimeSpan.FromSeconds(30);
                    var response = client.GetAsync(apiUrl).Result;

                    if (response.StatusCode == HttpStatusCode.NotFound)
                    {
                        return null;
                    }

                    if (response.IsSuccessStatusCode)
                    {
                        var jsonResponse = response.Content.ReadAsStringAsync().Result;
                        var versionInfo = JsonConvert.DeserializeObject<TranspilerVersionResponse>(jsonResponse);
                        return versionInfo?.Version;
                    }
                    else
                    {
                        Console.ForegroundColor = ConsoleColor.Yellow;
                        Console.WriteLine($"  Warning: API returned status {response.StatusCode}");
                        Console.ForegroundColor = ConsoleColor.Gray;
                    }
                }
            }
            catch (Exception ex)
            {
                Console.ForegroundColor = ConsoleColor.Yellow;
                Console.WriteLine($"  Warning: Could not fetch version from server: {ex.Message}");
                Console.ForegroundColor = ConsoleColor.Gray;
            }

            return null;
        }

