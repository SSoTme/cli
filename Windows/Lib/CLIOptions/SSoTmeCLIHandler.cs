        private bool ListTranspilerVersions(string transpilerName)
        {
            EnsureRemoteToolsInitialized();
            var remoteToolsDir = GetRemoteToolsDir();
            var toolsJsonPath = Path.Combine(remoteToolsDir, "ssotme-tools.json");
            if (!File.Exists(toolsJsonPath))
            {
                Console.WriteLine("No remote tools index found. Run 'ssotme -refreshTools' to download it.");
                return false;
            }

            var root = Newtonsoft.Json.Linq.JObject.Parse(File.ReadAllText(toolsJsonPath));
            var transpilers = (root["transpilerVersions"] ?? root["transpilers"]) as Newtonsoft.Json.Linq.JObject;
            if (transpilers == null)
            {
                Console.WriteLine("Remote tools index has no 'transpilers' section.");
                return false;
            }

            var suffix = "/" + transpilerName;
            var candidates = transpilers.Properties()
                .Where(p => p.Name == transpilerName || p.Name.EndsWith(suffix))
                .Select(p => p.Name)
                .ToList();

            if (candidates.Count == 0)
            {
                // Not in remote tools index — fall back to RabbitMQ
                return false;
            }

            if (candidates.Count > 1)
            {
                var best = SelectBestCandidate(candidates);
                Console.WriteLine($"Warning: '{transpilerName}' matched multiple tools: {String.Join(", ", candidates)}. Using '{best}'. Provide a fully-qualified name to suppress this warning.");
                candidates = new List<string> { best };
            }

            var versions = transpilers[candidates[0]] as Newtonsoft.Json.Linq.JObject;
            if (versions == null)
            {
                Console.WriteLine($"No versions found for '{candidates[0]}'.");
                return true;
            }

            var versionEntries = versions.Properties()
                .Select(p => new { Key = p.Name, Value = p.Value })
                .OrderByDescending(v => ParseVersionKey(v.Key, this.debug))
                .ToList();

            var overrideUrl = TryGetUrlFromFileUrls(transpilerName);

            Console.WriteLine($"Available versions for {candidates[0]}:");
            Console.WriteLine();
            bool hasHead = false;
            foreach (var entry in versionEntries)
            {
                bool isHead = entry.Value["metaData"]?["isHeadVersion"]?.Value<bool>() == true;
                if (isHead) hasHead = true;
                var mark = isHead ? " (latest)" : "";
                var postUrl = entry.Value["urls"]?["post"]?.Value<string>() ?? "(no url)";
                Console.WriteLine($"  {entry.Key}{mark}");
                Console.WriteLine($"    url: {postUrl}");
            }
            Console.WriteLine();
            if (!String.IsNullOrEmpty(overrideUrl))
            {
                Console.WriteLine($"  * globally overridden via effortless -setUrl {transpilerName}={overrideUrl}");
                Console.WriteLine($"    run 'effortless -removeUrl {transpilerName}' to reset");
                Console.WriteLine();
            }
            Console.WriteLine($"Run with: effortless {transpilerName}/<versionKey>");
            if (hasHead) Console.WriteLine($"Run latest: effortless {transpilerName}");
            return true;
        }

        /// <summary>
        /// Returns the PinnedVersion for the tool whose CommandLine starts with <paramref name="toolName"/> (short or qualified),
        /// or null if not found / not pinned.
        /// </summary>
        /// <summary>
        /// Checks if a CommandLine tool part matches a given tool name.
        /// Handles short names ("airtable-to-rulebook"), qualified names ("effortless/effortless/airtable-to-rulebook"),
        /// and qualified names with version ("effortless/effortless/airtable-to-rulebook/v2025.12.25.1915").
        /// </summary>
        private static bool ToolNameMatches(string cmdTool, string toolName)
        {
            if (String.Equals(cmdTool, toolName, StringComparison.OrdinalIgnoreCase)) return true;
            // Extract short name from both and compare
            var cmdShort = cmdTool.Contains('/') ? cmdTool.Substring(cmdTool.LastIndexOf('/') + 1) : cmdTool;
            var toolShort = toolName.Contains('/') ? toolName.Substring(toolName.LastIndexOf('/') + 1) : toolName;
            if (String.Equals(cmdShort, toolShort, StringComparison.OrdinalIgnoreCase)) return true;
            // cmdTool might be "author/package/tool/version" — strip trailing version segment and retry
            if (cmdTool.Contains('/'))
            {
                var cmdWithoutVersion = cmdTool.Substring(0, cmdTool.LastIndexOf('/'));
                var cmdToolName = cmdWithoutVersion.Contains('/') ? cmdWithoutVersion.Substring(cmdWithoutVersion.LastIndexOf('/') + 1) : cmdWithoutVersion;
                if (String.Equals(cmdToolName, toolShort, StringComparison.OrdinalIgnoreCase)) return true;
                if (String.Equals(cmdWithoutVersion, toolName, StringComparison.OrdinalIgnoreCase)) return true;
            }
            return false;
        }

        /// <summary>
        /// Given multiple transpiler matches, picks the one whose RelativePath matches
        /// the current working directory relative to the project root. Falls back to first match.
        /// </summary>
        private static ProjectTranspiler DisambiguateByCurrentDirectory(List<ProjectTranspiler> matches, string projectRootPath)
        {
            if (matches == null || matches.Count == 0) return null;
            if (matches.Count == 1 || string.IsNullOrEmpty(projectRootPath)) return matches.FirstOrDefault();

            var cwd = Environment.CurrentDirectory.Replace("\\", "/").TrimEnd('/');
            var root = projectRootPath.Replace("\\", "/").TrimEnd('/');
            if (cwd.StartsWith(root, StringComparison.OrdinalIgnoreCase))
            {
                var relativeCwd = "/" + cwd.Substring(root.Length).TrimStart('/');
                var match = matches.FirstOrDefault(pt =>
                    String.Equals(pt.RelativePath?.Replace("\\", "/").TrimEnd('/'),
                        relativeCwd.TrimEnd('/'), StringComparison.OrdinalIgnoreCase));
                if (match != null) return match;
            }
            return matches.FirstOrDefault();
        }

        private ProjectTranspiler GetMatchingTranspilerForTool(string toolName)
        {
            var transpilers = this.AICaptureProject?.ProjectTranspilers;
            if (transpilers == null || String.IsNullOrEmpty(toolName)) return null;
            var matches = transpilers.Where(pt => {
                var cmdTool = pt.CommandLine?.Split(' ').FirstOrDefault() ?? "";
                return ToolNameMatches(cmdTool, toolName);
            }).ToList();

            return DisambiguateByCurrentDirectory(matches, this.AICaptureProject?.RootPath);
        }

        private string GetPinnedVersionForTool(string toolName)
        {
            var pt = GetMatchingTranspilerForTool(toolName);
            if (pt == null) return null;

            // Hard pin takes absolute precedence (user explicitly said "stay here forever")
            if (!String.IsNullOrEmpty(pt.PinnedVersion)) return pt.PinnedVersion;

            // Soft pin: use the last version that ran successfully
            return pt.LastVersionUsed;
        }

        /// <summary>
        /// Handles `ssotme {toolName} -upgrade`: resolves head version, updates PinnedVersion in ssotme.json.
        /// When no toolName is given, upgrades all tools in the project to the latest version.
        /// Always refreshes the remote tools index first.
        /// </summary>
        private void HandleUpgradeCommand()
        {
            // Always refresh the remote tools index before upgrading
            this.RefreshRemoteTools();

            var toolName = this._rawTranspilerArg ?? this.transpiler;
            var project = this.AICaptureProject ?? SSoTmeProject.TryToLoad(new DirectoryInfo(Environment.CurrentDirectory));
            if (project == null)
            {
                ShowError("No effortless.json project found in this directory.");
                return;
            }

            if (String.IsNullOrEmpty(toolName))
            {
                // Upgrade all tools in the project
                UpgradeAllTools(project);
                return;
            }

            UpgradeSingleTool(toolName, project);
        }

        private void UpgradeSingleTool(string toolName, SSoTmeProject project)
        {
            var remoteToolsDir = GetRemoteToolsDir();
            // Force head version lookup (no pin)
            var url = TryGetUrlFromRemoteToolsJson(toolName, remoteToolsDir, pinnedVersion: null);
            if (url == null)
            {
                ShowError($"Could not resolve tool '{toolName}' from the remote tools index.");
                return;
            }
            var newVersion = this.ResolvedVersionKey;
            var allMatches = project.ProjectTranspilers?
                .Where(pt => {
                    var cmdTool = pt.CommandLine?.Split(' ').FirstOrDefault() ?? "";
                    return ToolNameMatches(cmdTool, toolName);
                }).ToList();
            var matched = DisambiguateByCurrentDirectory(allMatches, project.RootPath);
            if (matched == null)
            {
                ShowError($"No installed transpiler matching '{toolName}' found in effortless.json.");
                return;
            }
            var oldVersion = matched.PinnedVersion ?? matched.LastVersionUsed ?? "(unpinned)";
            matched.PinnedVersion = newVersion;
            project.Save();
            Console.WriteLine($"Upgraded {toolName}: {oldVersion} → {newVersion}");
        }

        /// <summary>
        /// Handles `ssotme -upgradeAll`: refreshes the remote tools index, then upgrades every
        /// transpiler in the current project to the latest version.
        /// </summary>
        private void HandleUpgradeAllCommand()
        {
            this.RefreshRemoteTools();

            var project = this.AICaptureProject ?? SSoTmeProject.TryToLoad(new DirectoryInfo(Environment.CurrentDirectory));
            if (project == null)
            {
                ShowError("No effortless.json project found in this directory.");
                return;
            }

            UpgradeAllTools(project);
        }

        private void UpgradeAllTools(SSoTmeProject project)
        {
            var transpilers = project.ProjectTranspilers;
            if (transpilers == null || !transpilers.Any())
            {
                Console.WriteLine("No transpilers found in effortless.json.");
                return;
            }

            var remoteToolsDir = GetRemoteToolsDir();
            int upgraded = 0;
            int skipped = 0;

            foreach (var pt in transpilers)
            {
                var cmdTool = pt.CommandLine?.Split(' ').FirstOrDefault() ?? "";
                if (String.IsNullOrEmpty(cmdTool)) continue;

                // Skip -exec commands — they are local scripts, not remote tools
                if (cmdTool.Equals("-exec", StringComparison.OrdinalIgnoreCase) ||
                    cmdTool.Equals("-execute", StringComparison.OrdinalIgnoreCase))
                {
                    continue;
                }

                var url = TryGetUrlFromRemoteToolsJson(cmdTool, remoteToolsDir, pinnedVersion: null);
                if (url == null)
                {
                    Console.WriteLine($"  SKIP {cmdTool} — not found in remote tools index");
                    skipped++;
                    continue;
                }

                var newVersion = this.ResolvedVersionKey;
                var oldVersion = pt.PinnedVersion ?? pt.LastVersionUsed ?? "(unpinned)";

                if (String.Equals(oldVersion, newVersion))
                {
                    Console.WriteLine($"  OK   {cmdTool} — already at {newVersion}");
                    continue;
                }

                pt.PinnedVersion = newVersion;
                Console.WriteLine($"  UP   {cmdTool}: {oldVersion} → {newVersion}");
                upgraded++;
            }

            project.Save();
            Console.WriteLine($"\nUpgraded {upgraded} tool(s)" + (skipped > 0 ? $", skipped {skipped}" : "") + ".");
        }

        private void HandleUpgradeCliCommand()
        {
            Console.WriteLine("Checking GitHub for the latest CLI version...");

            string latestTag = null;
            string downloadUrl = null;
            try
            {
                (latestTag, downloadUrl) = FetchLatestGitHubRelease();
            }
            catch (Exception ex)
            {
                Console.WriteLine($"Could not reach GitHub: {ex.Message}");
                Console.WriteLine($"Download manually: https://github.com/EffortlessAPI/cli/releases/latest");
                return;
            }

            if (String.IsNullOrEmpty(latestTag))
            {
                Console.WriteLine("Could not determine the latest version from GitHub.");
                return;
            }

            var latestVersion = ParseCliVersion(latestTag);
            var currentVersion = ParseCliVersion(this.CLI_VERSION);

            if (latestVersion.CompareTo(currentVersion) <= 0)
            {
                Console.WriteLine($"Already on the latest version ({this.CLI_VERSION}).");
                return;
            }

            Console.WriteLine($"New version available: {latestTag}  (you have {this.CLI_VERSION})");

            // First attempt: download and launch the platform-native installer from GitHub
            if (!String.IsNullOrEmpty(downloadUrl))
            {
                string installerPath = null;
                try
                {
                    Console.Write("Downloading installer from GitHub...");
                    installerPath = DownloadInstallerToTemp(downloadUrl);
                }
                catch (Exception ex)
                {
                    Console.WriteLine($"\nDownload failed: {ex.Message}");
                }

                if (installerPath != null)
                {
                    try
                    {
                        LaunchInstaller(installerPath);
                        Console.WriteLine("Installer launched — follow the prompts to complete the upgrade.");
                        return;
                    }
                    catch (Exception ex)
                    {
                        Console.WriteLine($"Could not launch installer: {ex.Message}");
                        Console.WriteLine($"Installer saved to: {installerPath}");
                        return;
                    }
                }
            }

            // Fallback: npm (for platforms without a native installer)
            var npmPath = FindExecutableOnPath(RuntimeInformation.IsOSPlatform(OSPlatform.Windows) ? "npm.cmd" : "npm")
                       ?? FindExecutableOnPath("npm");
            if (npmPath != null)
                Console.WriteLine($"To upgrade:  npm install -g ssotme@latest");
            else
                Console.WriteLine($"Download from: https://github.com/EffortlessAPI/cli/releases/latest");
        }

        private (string tag, string downloadUrl) FetchLatestGitHubRelease()
        {
            using var client = new System.Net.Http.HttpClient();
            client.Timeout = TimeSpan.FromSeconds(10);
            client.DefaultRequestHeaders.Add("User-Agent", "ssotme-cli");
            var json = client.GetStringAsync("https://api.github.com/repos/EffortlessAPI/cli/releases/latest").GetAwaiter().GetResult();
            var root = Newtonsoft.Json.Linq.JObject.Parse(json);
            var tag = root["tag_name"]?.Value<string>();

            var assetName = GetPlatformInstallerName();
            if (assetName == null) return (tag, null);

            var assets = root["assets"] as Newtonsoft.Json.Linq.JArray;
            var match = assets?.FirstOrDefault(a => String.Equals(a["name"]?.Value<string>(), assetName, StringComparison.OrdinalIgnoreCase));
            return (tag, match?["browser_download_url"]?.Value<string>());
        }

        private string GetPlatformInstallerName()
        {
            if (RuntimeInformation.IsOSPlatform(OSPlatform.Windows))
                return RuntimeInformation.ProcessArchitecture == Architecture.Arm64
                    ? "SSoTme-Installer_win-arm64.msi"
                    : "SSoTme-Installer_win-x64.msi";
            if (RuntimeInformation.IsOSPlatform(OSPlatform.OSX))
                return RuntimeInformation.ProcessArchitecture == Architecture.Arm64
                    ? "SSoTme-Installer-arm64.pkg"
                    : "SSoTme-Installer-x86_64.pkg";
            return null;
        }

        private string DownloadInstallerToTemp(string url)
        {
            var ext = Path.GetExtension(new Uri(url).AbsolutePath);
            var tempPath = Path.Combine(Path.GetTempPath(), $"ssotme-installer{ext}");
            using var client = new System.Net.Http.HttpClient();
            client.Timeout = TimeSpan.FromMinutes(5);
            client.DefaultRequestHeaders.Add("User-Agent", "ssotme-cli");
            using var response = client.GetAsync(url, System.Net.Http.HttpCompletionOption.ResponseHeadersRead).GetAwaiter().GetResult();
            response.EnsureSuccessStatusCode();

            var totalBytes = response.Content.Headers.ContentLength;
            if (totalBytes.HasValue)
                Console.Write($" ({totalBytes.Value / 1024 / 1024} MB)");
            Console.Write(" ");

            using var stream = response.Content.ReadAsStreamAsync().GetAwaiter().GetResult();
            using var file = File.Create(tempPath);
            var buffer = new byte[81920];
            long downloaded = 0;
            int lastPct = -1;
            int read;
            while ((read = stream.Read(buffer, 0, buffer.Length)) > 0)
            {
                file.Write(buffer, 0, read);
                downloaded += read;
                if (totalBytes.HasValue)
                {
                    var pct = (int)(downloaded * 100 / totalBytes.Value);
                    if (pct / 10 != lastPct / 10) { Console.Write($"{pct}% "); lastPct = pct; }
                }
            }
            Console.WriteLine("done.");
            return tempPath;
        }

        private void LaunchInstaller(string installerPath)
        {
            var ext = Path.GetExtension(installerPath).ToLowerInvariant();
            if (ext == ".msi")
            {
                System.Diagnostics.Process.Start(new System.Diagnostics.ProcessStartInfo
                {
                    FileName = installerPath,
                    UseShellExecute = true
                });
            }
            else if (ext == ".pkg")
            {
                System.Diagnostics.Process.Start(new System.Diagnostics.ProcessStartInfo
                {
                    FileName = "open",
                    Arguments = $"\"{installerPath}\"",
                    UseShellExecute = false
                });
            }
            else
            {
                throw new InvalidOperationException($"Unknown installer type: {ext}");
            }
        }

        private string FindExecutableOnPath(string name)
        {
            var pathVar = Environment.GetEnvironmentVariable("PATH") ?? "";
            foreach (var dir in pathVar.Split(Path.PathSeparator))
            {
                try
                {
                    var candidate = Path.Combine(dir, name);
                    if (File.Exists(candidate)) return candidate;
                }
                catch { }
            }
            return null;
        }

        private string FetchLatestGitHubTag()
        {
            using var client = new System.Net.Http.HttpClient();
            client.Timeout = TimeSpan.FromSeconds(10);
            client.DefaultRequestHeaders.Add("User-Agent", "ssotme-cli");
            var json = client.GetStringAsync("https://api.github.com/repos/EffortlessAPI/cli/releases/latest").GetAwaiter().GetResult();
            var root = Newtonsoft.Json.Linq.JObject.Parse(json);
            return root["tag_name"]?.Value<string>();
        }

        // Parses both "2026-04-16.17.57" (CLI_VERSION) and "v2026.04.16.1757" (bridge) into a comparable tuple.
        private (int, int, int, int) ParseCliVersion(string version)
        {
            try
            {
                var s = (version ?? "").TrimStart('v');
                var m = Regex.Match(s, @"^(\d{4})-(\d{2})-(\d{2})\.(\d{1,2})\.(\d{2})$");
                if (m.Success)
                {
                    int hhmm = int.Parse(m.Groups[4].Value) * 100 + int.Parse(m.Groups[5].Value);
                    return (int.Parse(m.Groups[1].Value), int.Parse(m.Groups[2].Value), int.Parse(m.Groups[3].Value), hhmm);
                }
                return ParseVersionKey(s, false);
            }
            catch { return (0, 0, 0, 0); }
        }

        private void CheckForGitHubUpdate()
        {
            try
            {
                var ssotmeDir = SSOTMEKey.SSoTmeDir.FullName;
                var cachePath = Path.Combine(ssotmeDir, "github_version_check.json");

                string latestTag = null;
                if (File.Exists(cachePath))
                {
                    try
                    {
                        var cached = Newtonsoft.Json.Linq.JObject.Parse(File.ReadAllText(cachePath));
                        var lastCheck = cached["lastCheck"]?.Value<DateTime>();
                        if (lastCheck.HasValue && (DateTime.UtcNow - lastCheck.Value).TotalHours < 24)
                            latestTag = cached["latestVersion"]?.Value<string>();
                    }
                    catch { }
                }

                if (latestTag == null)
                {
                    latestTag = FetchLatestGitHubTag();
                    var cacheJson = new Newtonsoft.Json.Linq.JObject
                    {
                        ["lastCheck"] = DateTime.UtcNow,
                        ["latestVersion"] = latestTag
                    };
                    File.WriteAllText(cachePath, cacheJson.ToString());
                }

                if (String.IsNullOrEmpty(latestTag)) return;

                var latestVersion = ParseCliVersion(latestTag);
                var currentVersion = ParseCliVersion(this.CLI_VERSION);

                if (latestVersion.CompareTo(currentVersion) > 0)
                {
                    Console.ForegroundColor = ConsoleColor.Yellow;
                    Console.WriteLine($"A new version of the effortless CLI is available: {latestTag}  (you have {this.CLI_VERSION})");
                    Console.WriteLine($"Run `effortless -upgradeCli` to see upgrade instructions.");
                    Console.ResetColor();
                }
            }
            catch { /* best-effort */ }
        }

        private string TryGetUrlFromRemoteTools(string transpilerName)
        {
            // Prevent recursive calls from internal handlers, skip for non-transpile commands, and skip empty names
            if (skipRemoteToolsLookup || IsManagementCommand || String.IsNullOrEmpty(transpilerName)) return null;

            _suppressGenericToolNotFoundError = false;

            try
            {
                if (this.debug)
                {
                    var effectiveTranspilersUrl = TryGetUrlFromFileUrls(SSoTmeCLIHandler.TRANSPILERS_LISTER_TOOL_NAME) ?? SSoTmeCLIHandler.LATEST_TRANSPILERS_LISTER_URL;
                    Console.WriteLine($"DEBUG: Checking remote_tools for '{transpilerName}' (transpilers url: {effectiveTranspilersUrl})");
                }
                EnsureRemoteToolsInitialized();
                var remoteToolsDir = GetRemoteToolsDir();

                // If the CLI version has changed since the last refresh, bust the cache
                var cliVersionPath = Path.Combine(remoteToolsDir, "cli_version");
                var cachedCliVersion = File.Exists(cliVersionPath) ? File.ReadAllText(cliVersionPath).Trim() : null;
                // Check if the cached tools index is empty (e.g. from a failed prior refresh)
                var toolsJsonPath = Path.Combine(remoteToolsDir, "ssotme-tools.json");
                bool indexIsEmpty = false;
                if (File.Exists(toolsJsonPath))
                {
                    try
                    {
                        var root = Newtonsoft.Json.Linq.JObject.Parse(File.ReadAllText(toolsJsonPath));
                        var transpilers = (root["transpilerVersions"] ?? root["transpilers"]) as Newtonsoft.Json.Linq.JObject;
                        indexIsEmpty = transpilers == null || !transpilers.HasValues;
                    }
                    catch { indexIsEmpty = true; }
                }

                if (cachedCliVersion != this.CLI_VERSION)
                {
                    if (this.debug) Console.WriteLine($"DEBUG: CLI version changed ({cachedCliVersion ?? "none"} → {this.CLI_VERSION}), forcing remote tools refresh");
                    _hasRunRemoteToolsUpdate = false;
                }
                else if (indexIsEmpty)
                {
                    if (this.debug) Console.WriteLine($"DEBUG: ssotme-tools.json is empty, forcing remote tools refresh");
                    _hasRunRemoteToolsUpdate = false;
                }
                else
                {
                    // Index is current and non-empty — no refresh needed regardless of whether this tool is in it
                    _hasRunRemoteToolsUpdate = true;
                }

                // Look up the pinned version for this tool from the current project (skip when -latest forces head)
                var pinnedVersion = this.latest ? null : GetPinnedVersionForTool(transpilerName);

                var url = TryGetUrlFromRemoteToolsJson(transpilerName, remoteToolsDir, pinnedVersion);
                if (this._suppressGenericToolNotFoundError) return null;  // specific error already printed
                if (url != null && cachedCliVersion == this.CLI_VERSION)
                {
                    if (this.debug) Console.WriteLine($"DEBUG: matched '{transpilerName}' to remote tool (cached index) → {url}");
                    return url;
                }

                if (!_hasRunRemoteToolsUpdate)
                {
                    if (this.debug) Console.WriteLine($"DEBUG: no match in cached index for '{transpilerName}', refreshing remote tools...");
                    CliLog.LogLine("Refreshing CLI tool URL index...");
                    RunRemoteToolsRefresh(remoteToolsDir, cliVersionPath, cachedCliVersion);

                    if (this.debug) Console.WriteLine($"DEBUG: refresh complete, rechecking for '{transpilerName}'");

                    url = TryGetUrlFromRemoteToolsJson(transpilerName, remoteToolsDir, pinnedVersion);
                    if (this._suppressGenericToolNotFoundError) return null;  // specific error already printed
                    if (this.debug)
                    {
                        if (url != null) Console.WriteLine($"DEBUG: matched '{transpilerName}' to remote tool (after refresh) → {url}");
                        else Console.WriteLine($"DEBUG: no remote tool match for '{transpilerName}', falling back to RabbitMQ");
                    }
                }
                else if (this.debug)
                {
                    Console.WriteLine($"DEBUG: remote_tools already refreshed this session, skipping");
                    if (url == null) Console.WriteLine($"DEBUG: no remote tool match for '{transpilerName}', falling back to RabbitMQ");
                }

                return url;
            }
            catch (Exception ex)
            {
                Console.WriteLine($"Warning: Error resolving transpiler from remote tools: {ex.Message}");
                return null;
            }
        }

        private void ProcessCommandLine(string commandLine)
        {
            var cmd = $"{commandLine} ";
            var executable = cmd.Substring(0, cmd.IndexOf(" "));
            var args = cmd.Substring(executable.Length + 1);
            var psi = new ProcessStartInfo(executable, args);
            try
            {
                var process = Process.Start(psi);
                process.WaitForExit(this.waitTimeout);
                if (!process.HasExited)
                {
                    process.Close();
                    throw new NoStackException(String.Format("Timed out waiting for process to complete: {0}", commandLine));
                }
            }
            catch (System.ComponentModel.Win32Exception ex)
            {
                throw new NoStackException($"The system couldn't find the command \"{executable}\"");
            }
        }

