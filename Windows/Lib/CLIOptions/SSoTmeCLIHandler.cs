                            this.AICaptureProject?.Rebuild(Environment.CurrentDirectory, this.includeDisabled, this.transpilerGroup, this.buildOnTrigger, this.copilotConnect, this.buildLocal, this.debug);
                            Console.WriteLine("Auto-build completed successfully.");
                        }
                        catch (Exception ex)
                        {
                            ShowError($"WARNING: Auto-build failed but continuing with install: {ex.Message}", ConsoleColor.Yellow);
                        }
                    }

                    this.LoadInputFiles();

                    var key = SSOTMEKey.GetSSoTmeKey(this.runAs);
                    if (!String.IsNullOrEmpty(this.account))
                    {
                        if (this.debug)
                        {
                            Console.WriteLine($"DEBUG: Account is set to '{this.account}', looking up API key...");
                        }

                        if (key.APIKeys.ContainsKey(this.account))
                        {
                            var apiKeyValue = key.APIKeys[this.account];

                            // Check if the API key is a simple string or JSON/complex format
                            if (apiKeyValue.TrimStart().StartsWith("{") || apiKeyValue.TrimStart().StartsWith("["))
                            {
                                // JSON format detected
                                throw new Exception($"The API key for account '{this.account}' is in JSON format and cannot be used directly as apiKey parameter. Please store simple string API keys in the format: ssotme -setAccountAPIKey={this.account}/YOUR_API_KEY");
                            }

                            if (this.debug)
                            {
                                Console.WriteLine($"DEBUG: Adding apiKey parameter from account '{this.account}'");
                            }

                            this.parameters.Add(String.Format("apiKey={0}", apiKeyValue));
                        }
                        else
                        {
                            if (this.debug)
                            {
                                Console.WriteLine($"DEBUG: No API key found for account '{this.account}'");
                            }
                        }
                    }
                    else
                    {
                        if (this.debug)
                        {
                            Console.WriteLine($"DEBUG: No account specified, not adding API key");
                        }
                    }
                    if (!ReferenceEquals(this.FileSet, null))
                    {
                        this.ZFSFileSetFile = this.FileSet.FileSetFiles.FirstOrDefault(fodFileSetFile => fodFileSetFile.RelativePath.EndsWith(".zfs", StringComparison.OrdinalIgnoreCase));
                    }
                }
            }
            catch (Exception ex)
            {
                var curColor = Console.ForegroundColor;
                var exceptionMessage = ex.Message ?? "";
                var isTranspilerNotFound = exceptionMessage.Contains("Could not find transpiler") || exceptionMessage.Contains("transpiler status message");

                // For "transpiler not found" errors, show a simpler message without the warning box
                if (isTranspilerNotFound)
                {
                    Console.ForegroundColor = ConsoleColor.Red;
                    Console.WriteLine("\nERROR: Tool '{0}' does not exist.", this.transpiler);
                    Console.WriteLine("\nThe tool was not found locally or on the SSoTme server.");
                    Console.WriteLine("\nTo see available tools, run:");
                    Console.WriteLine("  ssotme -list-tool-urls");
                    Console.WriteLine("\nTo add a new tool URL mapping, run:");
                    Console.WriteLine("  ssotme -set-tool-url <tool-name>=<url>");
                    Console.ForegroundColor = curColor;
                    this.SuppressTranspile = true;
                    return;
                }

                // For other transpiler errors, show the full warning box
                // Provide helpful context about the error
                Console.ForegroundColor = ConsoleColor.Yellow;
                Console.WriteLine("\n=======================================================");
                Console.WriteLine("*** TRANSPILER ERROR ***");
                Console.WriteLine("=======================================================");
                Console.WriteLine(!String.IsNullOrEmpty(transpiler)
                    ? $"This is likely an issue with the transpiler '{transpiler}', not with SSoTme."
                    : "This is likely an issue with the transpiler, not with SSoTme.");
                Console.WriteLine("The transpiler may have received invalid input or encountered an internal error.");
                Console.WriteLine("=======================================================\n");
                Console.ForegroundColor = curColor;

                Console.ForegroundColor = ConsoleColor.Red;
                var currentException = ex;
                while (!(currentException is null))
                {
                    Console.WriteLine("\n********************************\nERROR: {0}\n********************************\n\n", currentException.Message);
                    Console.WriteLine(currentException.StackTrace);
                    Console.WriteLine("\n\n");
                    if (currentException == ex.InnerException) break;
                    currentException = ex.InnerException;
                }
                Console.ForegroundColor = curColor;
                this.SuppressTranspile = true;
            }
        }

