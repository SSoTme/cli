        /// <summary>
        /// Walks up the directory tree from startPath looking for a directory with effortless.json or ssotme.json.
        /// Returns the directory path containing the project file, or null if not found.
        /// </summary>
        private static string TryFindNearestProjectRootFrom(string startPath)
        {
            if (string.IsNullOrWhiteSpace(startPath)) return null;
            try
            {
                var di = new DirectoryInfo(Path.GetFullPath(startPath));
                while (di != null)
                {
                    if (File.Exists(Path.Combine(di.FullName, "effortless.json")) ||
                        File.Exists(Path.Combine(di.FullName, "ssotme.json")))
                        return di.FullName;
                    di = di.Parent;
                }
            }
            catch
            {
                // Path may be invalid, return null
            }
            return null;
        }

        /// <summary>
        /// Ensures a path ends with a directory separator to prevent false-positive prefix matches.
        /// For example, C:\proj vs C:\proj-other
        /// </summary>
        private static string WithTrailingSeparator(string path)
        {
            if (string.IsNullOrEmpty(path)) return path;
            var last = path[path.Length - 1];
            if (last != Path.DirectorySeparatorChar && last != Path.AltDirectorySeparatorChar)
            {
                return path + Path.DirectorySeparatorChar;
            }
            return path;
        }

        /// <summary>
        /// Validates that a file path is within the project scope.
        /// Throws NoStackException if the path attempts to access files outside the project.
        /// </summary>
        /// <param name="filePath">The file path to validate</param>
        /// <param name="projectName">The project name for error messages</param>
        /// <param name="projectRoot">The project root directory (optional)</param>
        /// <param name="allowedFolders">Additional folders that are allowed (e.g., parent ssotme projects)</param>
        private void ValidatePathIsInProjectScope(string filePath, string projectName, string projectRoot = null, List<string> allowedFolders = null)
        {
            // If no project is loaded, we can't validate scope - allow the operation
            // This handles cases like -init, -listSeeds, etc. that don't need a project
            if (String.IsNullOrEmpty(projectRoot))
            {
                return;
            }

            try
            {
                // Resolve both paths to absolute paths to handle .. and symlinks
                // Use WithTrailingSeparator to prevent false-positive prefix matches
                var absoluteProjectRoot = WithTrailingSeparator(Path.GetFullPath(projectRoot));
                var absoluteFilePath = WithTrailingSeparator(Path.GetFullPath(Path.Combine(Environment.CurrentDirectory, filePath)));

                // Check if the file path starts with the project root
                // Use case-insensitive comparison for Windows compatibility
                if (absoluteFilePath.StartsWith(absoluteProjectRoot, StringComparison.OrdinalIgnoreCase))
                {
                    return; // Path is within project root - allowed
                }

                // Check if the file path is within any of the allowed folders
                if (allowedFolders != null && allowedFolders.Any())
                {
                    foreach (var allowedFolder in allowedFolders)
                    {
                        var absoluteAllowedFolder = WithTrailingSeparator(Path.GetFullPath(allowedFolder));
                        if (absoluteFilePath.StartsWith(absoluteAllowedFolder, StringComparison.OrdinalIgnoreCase))
                        {
                            Console.WriteLine($"Allowing access to parent ssotme project: {absoluteAllowedFolder}");
                            return; // Path is within an allowed folder - allowed
                        }
                    }
                }

                // Path is not within project root or any allowed folders
                throw new NoStackException(
                    $"Access denied: The path '{filePath}' resolves to a directory outside the current project scope ({absoluteFilePath}).\n" +
                    $"Current project: {projectName} ({absoluteProjectRoot})\n" +
                    $"For security reasons, ssotme can only access files within the project directory.");
            }
            catch (NoStackException)
            {
                // Re-throw our custom exceptions
                throw;
            }
            catch (Exception ex)
            {
                // Handle path resolution errors
                throw new NoStackException($"Error validating path '{filePath}': {ex.Message}");
            }
        }

        private void ImportFile(string filePattern, FileSet fs)
        {
            var fileNameReplacement = String.Empty;
            if (filePattern.Contains("="))
            {
                fileNameReplacement = filePattern.Substring(0, filePattern.IndexOf("="));
                filePattern = filePattern.Substring(filePattern.IndexOf("=") + 1);
            }

            var directoryPath = Path.GetDirectoryName(filePattern);
            if (!String.IsNullOrEmpty(directoryPath))
            {
                // Get the effective project root. When building (ProjectTranspiler.Rebuild),
                // AICaptureProject is set before parsing, so we use that. Otherwise, walk up
                // from current directory to find effortless.json/ssotme.json (for direct CLI usage).
                var effectiveProjectRoot = this.AICaptureProject?.RootPath
                                          ?? TryFindNearestProjectRootFrom(Environment.CurrentDirectory);

                if (string.IsNullOrEmpty(effectiveProjectRoot))
                {
                    throw new InvalidOperationException($"No parent 'effortless.json' found from {Environment.CurrentDirectory}");
                }

                var name = this.AICaptureProject?.Name
                           ?? new DirectoryInfo(effectiveProjectRoot).Name;

                // Parent-project allowance: Check if parent directory also has a project file
                List<string> allowedFolders = null;
                var rootDI = new DirectoryInfo(effectiveProjectRoot);
                var parentDI = rootDI.Parent;
                if (parentDI != null)
                {
                    if (File.Exists(Path.Combine(parentDI.FullName, "effortless.json")) ||
                        File.Exists(Path.Combine(parentDI.FullName, "ssotme.json")))
                    {
                        // Parent has project file, so allow reading from parent directory
                        allowedFolders = new List<string> { parentDI.FullName };
                    }
                }

                ValidatePathIsInProjectScope(directoryPath, name, effectiveProjectRoot, allowedFolders);
            }

            var di = new DirectoryInfo(Path.Combine(".", Path.GetDirectoryName(filePattern)));
            filePattern = Path.GetFileName(filePattern);
            var matchingFiles = new FileInfo[] { };
            if (di.Exists)
            {
                matchingFiles = di.GetFiles(filePattern);
            }
            if (!matchingFiles.Any() && !this.OptionalInputCLIInputs.Contains(filePattern))
            {
                var curColor = Console.ForegroundColor;
                Console.ForegroundColor = ConsoleColor.Yellow;
                Console.WriteLine("\n\nWARNING:\n\n - No INPUT files matched {0} in {1}\n", filePattern, di.FullName);
                var fsf = new FileSetFile();
                fsf.RelativePath = Path.GetFileName(filePattern);
                fs.FileSetFiles.Add(fsf);
                Console.ForegroundColor = curColor;
            }

            foreach (var matchingFileFI in matchingFiles)
            {
                var fsf = new FileSetFile();
                fsf.RelativePath = String.IsNullOrEmpty(fileNameReplacement) ? matchingFileFI.Name : fileNameReplacement;
                var projectRootLength = this.AICaptureProject.RootPath.Length;
                var matchingFileFILength = matchingFileFI.Directory.FullName.Length;
                var minLength = Math.Min(projectRootLength, matchingFileFILength);
                fsf.OriginalRelativePath = matchingFileFI.FullName.Substring(minLength).Replace("\\", "/");
                fs.FileSetFiles.Add(fsf);

                if (matchingFileFI.Exists)
                {
                    if (matchingFileFI.IsBinaryFile())
                    {
                        fsf.ZippedBinaryFileContents = File.ReadAllBytes(matchingFileFI.FullName).Zip();
                    }
                    else
                    {
                        fsf.ZippedFileContents = File.ReadAllText(matchingFileFI.FullName).Zip();
                    }
                }
                else
                {
                    var curColor = Console.ForegroundColor;
                    Console.ForegroundColor = ConsoleColor.Yellow;
                    Console.WriteLine("INPUT Format: {0} did not match any files in {1}", filePattern, di.FullName);
                    Console.ForegroundColor = curColor;
                }
            }
        }

        private void conditionallyPopulateTranspiler(SSOTMEPayload payload, string transpilerName)
        {
            // Always initialize the Transpiler property to avoid NullReferenceException
            if (payload.Transpiler == null)
            {
                payload.Transpiler = new Transpiler();

                // Set a name based on the transpiler string if available
                if (!string.IsNullOrEmpty(this.transpiler))
                {
                    payload.Transpiler.Name = this.transpiler;
                }
                else
                {
                    // Use passed transpiler name
                    payload.Transpiler.Name = transpilerName;
                }
            }
        }

        private async void AccountHolder_ReplyTo(object sender, SassyMQ.Lib.RabbitMQ.PayloadEventArgs<SSOTMEPayload> e)
        {
            // Console.WriteLine("DirectMessageQueue: {0},  Exchange: {1}, LexiconTerm: {2}, RoutingKey: {3}, SenderId: {4}",
            //     e.Payload.DirectMessageQueue, e.Payload.Exchange, e.Payload.LexiconTerm, e.Payload.RoutingKey, e.Payload.SenderId);
            var payload = AccountHolder.CreatePayload();
            payload.SaveCLIOptions(this);

            if (e.Payload.IsLexiconTerm(LexiconTermEnum.accountholder_ping_ssotmecoordinator))
            {
                if (!String.IsNullOrEmpty(this.targetUrl) )
                {
                    this.conditionallyPopulateTranspiler(payload, this.transpiler);

                    // Set LowerHyphenName for .zfs file creation after transpiler is populated
                    if (payload.Transpiler != null && !string.IsNullOrEmpty(payload.Transpiler.Name))
                    {
                        if (!string.IsNullOrEmpty(this.targetUrl))
                        {
                            // For remote transpilers, use sanitized URL as the filename
                            var sanitizedUrl = this.targetUrl.SanitizeUrlForFilename();
                            payload.Transpiler.LowerHyphenName = sanitizedUrl;
                            Console.WriteLine($"Setting remote transpiler .zfs name to SANITIZED URL: {sanitizedUrl}");
                        }
                        else
                        {
                            // For other transpilers, derive from name
                            payload.Transpiler.LowerHyphenName = SSoTmeProject.LowerHyphenName(payload.Transpiler.Name);
                            Console.WriteLine($"Setting transpiler .zfs name to DERIVED NAME: {payload.Transpiler.LowerHyphenName}");
                        }
                    }

                    using var client = new HttpClient();
                    payload.TranspileRequest = new TranspileRequest();
                    payload.TranspileRequest.ZippedInputFileSet = this.inputFileSetXml.Zip();
                    if (this.debug) Console.WriteLine($"DEBUG: POST {this.targetUrl}");
                    var response = await client.PostAsJsonAsync($"{this.targetUrl}", payload);
                    if (response != null)
                    {
                        var responseContent = await response.Content.ReadAsStringAsync();
                        var responsePayload = JsonConvert.DeserializeObject<SSOTMEPayload>(responseContent);

                        // Fix missing transpiler name from remote transpiler
                        if (responsePayload?.Transpiler == null)
                        {
                            responsePayload.Transpiler = new Transpiler();
                        }

                        if (string.IsNullOrEmpty(responsePayload.Transpiler.Name))
                        {
                            // Remote transpiler didn't set a name, use our sanitized URL
                            responsePayload.Transpiler.Name = this.transpiler;
                            responsePayload.Transpiler.LowerHyphenName = this.transpiler;
                        }

                        result = responsePayload;
                        if (result != null)
                        {
                            result.SSoTmeProject = this.AICaptureProject;
                            if (this.clean) result.CleanFileSet(deleteEmptyDirs: false);
                            else
                            {
                                var finalResult = result.SaveFileSet(this.skipClean);
                            }
                        }
                    }
                }
                else
                {
                    CoordinatorProxy = new DMProxy(e.Payload.DirectMessageQueue);
                    payload.TranspileRequest = new TranspileRequest();
                    payload.TranspileRequest.ZippedInputFileSet = this.inputFileSetXml.Zip();
                    payload.CLIInputFileContents = String.Empty;
                    AccountHolder.AccountHolderCommandLineTranspile(payload, CoordinatorProxy);
                }
            }
            else if (e.Payload.IsLexiconTerm(LexiconTermEnum.accountholder_commandlinetranspile_ssotmecoordinator) ||
                    (e.Payload.IsLexiconTerm(LexiconTermEnum.accountholder_requesttranspile_ssotmecoordinator)))
            {
                result = e.Payload;
            }
        }

        private async void ProxyRequest()
        {
            var payload = AccountHolder.CreatePayload();
            payload.SaveCLIOptions(this);
            this.conditionallyPopulateTranspiler(payload, this.transpiler);

            // -------- QUOTA: pre-transpile fetch --------
            // Only attempt when this is an HTTP-direct transpiler we resolved from the remote registry
            // (ResolvedToolName holds the canonical "author/package/tool" key). RabbitMQ-path and
            // unresolved tools get no quota enforcement — the transpiler just runs.
            // Null-safe: if any of these are missing, we skip silently.
            string quotaTranspilerKey = null;
            string quotaProjectUuid = null;
            bool quotaAttempted = false;
            if (this.debug)
            {
                Console.WriteLine($"DEBUG: Quota guard check: jwt={!String.IsNullOrEmpty(this.jwt)}, ResolvedToolName={(this.ResolvedToolName ?? "<null>")}, AICaptureProject={(this.AICaptureProject != null ? "set" : "null")}, SSoTmeProjectId={(this.AICaptureProject?.SSoTmeProjectId ?? "<null>")}, skipRemoteToolsLookup={this.skipRemoteToolsLookup}");
            }
            if (!String.IsNullOrEmpty(this.jwt)
                && !String.IsNullOrEmpty(this.ResolvedToolName)
                && this.AICaptureProject != null
                && !String.IsNullOrEmpty(this.AICaptureProject.SSoTmeProjectId)
                && this.AICaptureProject.SSoTmeProjectId != Guid.Empty.ToString()
                && !this.skipRemoteToolsLookup)
            {
                quotaTranspilerKey = this.ResolvedToolName;
                quotaProjectUuid = this.AICaptureProject.SSoTmeProjectId;
                try
                {
                    var quotaInfo = new SSoTme.OST.Lib.Services.MyEffortlessAPIService()
                        .GetQuota(this.jwt, quotaProjectUuid, quotaTranspilerKey);
                    if (quotaInfo != null && quotaInfo.Success && !quotaInfo.Free)
                    {
                        quotaAttempted = true;

                        // If the bridge says this transpile isn't allowed (no headroom even after
                        // subtracting this project's share), warn the user but still proceed
                        // per the warn-only enforcement policy.
                        if (!quotaInfo.IsAllowedToTranspile)
                        {
                            Console.ForegroundColor = ConsoleColor.Yellow;
                            Console.WriteLine($"WARNING: Your account is over its quota limit ({quotaInfo.CurrentUsed}/{quotaInfo.Limit} units). This transpile will proceed but may incur overage charges. Please contact us to upgrade your plan: https://effortlessapi.com/rulebook/waitlist");
                            Console.ResetColor();
                        }

                        // Inject params so the transpiler can check itself and surface a warning
                        // in its output if it exceeds the available headroom.
                        if (payload.CLIParams == null) payload.CLIParams = new List<string>();
                        payload.CLIParams.Add($"available_quota={quotaInfo.AvailableNativeCount.ToString(System.Globalization.CultureInfo.InvariantCulture)}");
                        payload.CLIParams.Add($"project_uuid={quotaProjectUuid}");
                        payload.CLIParams.Add($"has_unlimited_quota={(quotaInfo.HasUnlimitedQuota ? "true" : "false")}");
                        if (this.debug)
                            Console.WriteLine($"DEBUG: Quota for {quotaTranspilerKey}: {quotaInfo.CurrentUsed}/{quotaInfo.Limit} (peak {quotaInfo.Peak}), project prev {quotaInfo.ThisProjectTranspilerPrevious}, available native {quotaInfo.AvailableNativeCount}, allowed={quotaInfo.IsAllowedToTranspile}");
                    }
                    else if (this.debug)
                    {
                        Console.WriteLine($"DEBUG: getQuota returned null/failed (success={quotaInfo?.Success}, error={quotaInfo?.Error ?? "<null result>"})");
                    }
                }
                catch (Exception ex)
                {
                    if (this.debug) Console.WriteLine($"DEBUG: getQuota threw: {ex.Message}");
                }
            }
            // -------- end QUOTA: pre-transpile fetch --------

            // Set LowerHyphenName for .zfs file creation after transpiler is populated
            if (payload.Transpiler != null && !string.IsNullOrEmpty(payload.Transpiler.Name))
            {
                if (!string.IsNullOrEmpty(this.targetUrl))
                {
                    // For remote transpilers, use sanitized URL as the filename
                    var sanitizedUrl = this.targetUrl.SanitizeUrlForFilename();
                    payload.Transpiler.LowerHyphenName = sanitizedUrl;
                    // Console.WriteLine($"Setting remote transpiler .zfs name to SANITIZED URL: {sanitizedUrl}");
                }
                else
                {
                    // For other transpilers, derive from name
                    payload.Transpiler.LowerHyphenName = SSoTmeProject.LowerHyphenName(payload.Transpiler.Name);
                    // Console.WriteLine($"Setting transpiler .zfs name to DERIVED NAME: {payload.Transpiler.LowerHyphenName}");
                }
            }

            using var client = new HttpClient();
            client.Timeout = TimeSpan.FromMilliseconds(this.waitTimeout);
            payload.TranspileRequest = new TranspileRequest();
            payload.TranspileRequest.ZippedInputFileSet = this.inputFileSetXml.Zip();
            payload.CLIInputFileContents = string.Empty;
            if (this.debug) Console.WriteLine(String.IsNullOrEmpty(this.targetUrl)
                ? "DEBUG: POST https://proxy.effortlessapi.com/ (via RabbitMQ proxy)"
                : $"DEBUG: POST {this.targetUrl}");
            var postUrl = $"{this.targetUrl ?? "https://proxy.effortlessapi.com/"}";
            System.Net.Http.HttpResponseMessage response = null;
            var stopwatch = System.Diagnostics.Stopwatch.StartNew();
            const int maxRetries = 10;
            int retryCount = 0;
            int connectionRefusedCount = 0;
            const int maxConnectionRefused = 3; // ConnectionRefused likely means wrong URL, not cold boot

            while (stopwatch.ElapsedMilliseconds < this.waitTimeout && retryCount < maxRetries)
            {
                try
                {
                    response = await client.PostAsJsonAsync(postUrl, payload);
                }
                catch (TaskCanceledException)
                {
                    // HttpClient.Timeout elapsed — let the waitForCook timeout handler deal with it
                    return;
                }
                catch (System.Net.Http.HttpRequestException ex) when (
                    ex.InnerException is System.Net.Sockets.SocketException se && se.SocketErrorCode == System.Net.Sockets.SocketError.HostNotFound
                    || ex.Message.Contains("No such host"))
                {
                    retryCount++;
                    string failedHost;
                    try { failedHost = new Uri(postUrl).Host; } catch { failedHost = postUrl; }
                    CliLog.LogLine($"Host not found: {failedHost}. Retrying in 6 seconds... (attempt {retryCount}/{maxRetries})");
                    await System.Threading.Tasks.Task.Delay(6000);
                    continue;
                }
                catch (System.Net.Http.HttpRequestException ex) when (
                    ex.InnerException is System.Net.Sockets.SocketException se2 && (
                        se2.SocketErrorCode == System.Net.Sockets.SocketError.ConnectionReset ||
                        se2.SocketErrorCode == System.Net.Sockets.SocketError.ConnectionRefused ||
                        se2.SocketErrorCode == System.Net.Sockets.SocketError.NetworkUnreachable))
                {
                    retryCount++;
                    if (se2.SocketErrorCode == System.Net.Sockets.SocketError.ConnectionRefused)
                    {
                        connectionRefusedCount++;
                        if (connectionRefusedCount >= maxConnectionRefused)
                        {
                            Console.ForegroundColor = ConsoleColor.Red;
                            Console.WriteLine($"\nERROR: Connection refused {maxConnectionRefused} times for: {postUrl}");
                            Console.WriteLine("The transpiler URL may be incorrect or the service is not deployed.");
                            Console.WriteLine("If this tool has no PinnedVersion, add one to your project config or use -id to specify a version.");
                            Console.ResetColor();
                            return;
                        }
                    }
                    CliLog.LogLine($"Connection error ({se2.SocketErrorCode}). Retrying in 6 seconds... (attempt {retryCount}/{maxRetries})");
                    await System.Threading.Tasks.Task.Delay(6000);
                    continue;
                }
                catch (System.Net.Http.HttpRequestException ex) when (
                    ex.InnerException is System.Security.Authentication.AuthenticationException ||
                    ex.Message.Contains("SSL"))
                {
                    retryCount++;
                    CliLog.LogLine($"SSL connection error. Retrying in 6 seconds... (attempt {retryCount}/{maxRetries})", ConsoleColor.Yellow);
                    if (this.debug) Console.WriteLine($"DEBUG: {ex.Message}");
                    await System.Threading.Tasks.Task.Delay(6000);
                    continue;
                }

                // Retry on gateway timeout (remote transpiler still cold-booting)
                if (response.StatusCode == System.Net.HttpStatusCode.GatewayTimeout ||
                    response.StatusCode == System.Net.HttpStatusCode.BadGateway ||
                    response.StatusCode == System.Net.HttpStatusCode.ServiceUnavailable)
                {
                    retryCount++;
                    var retryContent = await response.Content.ReadAsStringAsync();
                    CliLog.LogLine($"Remote transpiler not ready ({response.StatusCode}). Retrying... (attempt {retryCount}/{maxRetries})", ConsoleColor.Yellow);
                    if (this.debug) Console.WriteLine($"DEBUG: Response body: {retryContent}");
                    await System.Threading.Tasks.Task.Delay(5000);
                    continue;
                }

                // Retry on server-side SSL errors (transient infrastructure issue)
                if (response.StatusCode == System.Net.HttpStatusCode.BadRequest)
                {
                    var badRequestContent = await response.Content.ReadAsStringAsync();
                    if (badRequestContent.Contains("SSL connection could not be established") ||
                        badRequestContent.Contains("SSL") && badRequestContent.Contains("error"))
                    {
                        retryCount++;
                        CliLog.LogLine($"Remote transpiler SSL error. Retrying in 6 seconds... (attempt {retryCount}/{maxRetries})", ConsoleColor.Yellow);
                        if (this.debug) Console.WriteLine($"DEBUG: Response body: {badRequestContent}");
                        await System.Threading.Tasks.Task.Delay(6000);
                        continue;
                    }
                }

                break; // Got a non-retryable response
            }

            if (retryCount >= maxRetries)
            {
                Console.ForegroundColor = ConsoleColor.Red;
                Console.WriteLine($"\nERROR: Failed to connect to transpiler after {maxRetries} attempts: {postUrl}");
                Console.ResetColor();
                return;
            }

            if (response == null)
            {
                return;
            }

            if (response != null)
            {
                var responseContent = await response.Content.ReadAsStringAsync();
                ClearBootLine();

                // Check if response is successful
                if (!response.IsSuccessStatusCode)
                {
                    this.result = new SSOTMEPayload
                    {
                        Exception = new Exception($"Proxy request failed with status {response.StatusCode}: {responseContent}")
                    };
                    return;
                }

                // Check if response content looks like JSON
                if (string.IsNullOrWhiteSpace(responseContent) ||
                    (!responseContent.TrimStart().StartsWith("{") && !responseContent.TrimStart().StartsWith("[")))
                {
                    // If response is plain text that might indicate transpiler status
                    if (responseContent.Trim().Equals("True", StringComparison.OrdinalIgnoreCase) ||
                        responseContent.Trim().Equals("False", StringComparison.OrdinalIgnoreCase) ||
                        responseContent.Trim().Contains("transpiler") ||
                        responseContent.Trim().Contains("inactive"))
                    {
                        this.result = new SSOTMEPayload
                        {
                            Exception = new Exception($"Proxy server returned transpiler status message: {responseContent.Trim()}")
                        };
                    }
                    else
                    {
                        this.result = new SSOTMEPayload
                        {
                            Exception = new Exception($"Proxy server returned non-JSON response: {responseContent}")
                        };
                    }
                    return;
                }
                try
                {
                    var responsePayload = JsonConvert.DeserializeObject<SSOTMEPayload>(responseContent);

                    // Check if transpiler returned an async task (TaskId at same level as Logs)
                    if (!string.IsNullOrEmpty(responsePayload?.TaskId) &&
                        string.Equals(responsePayload.TaskStatus, "pending", StringComparison.OrdinalIgnoreCase))
                    {
                        var taskPollUrl = $"{postUrl.TrimEnd('/')}/task/{responsePayload.TaskId}";
                        var pollInterval = 3000;

                        if (this.debug)
                            Console.WriteLine($"DEBUG: Transpiler returned TaskId={responsePayload.TaskId}, switching to poll mode");

                        CliLog.LogLine("Transpiler processing asynchronously, polling for result...", ConsoleColor.Cyan);

                        while (stopwatch.ElapsedMilliseconds < this.waitTimeout)
                        {
                            await System.Threading.Tasks.Task.Delay(pollInterval);

                            try
                            {
                                var pollResponse = await client.GetAsync(taskPollUrl);

                                if (pollResponse.StatusCode == System.Net.HttpStatusCode.NotFound)
                                {
                                    this.result = new SSOTMEPayload
                                    {
                                        Exception = new Exception("Async task not found on transpiler (container may have restarted). Please retry.")
                                    };
                                    return;
                                }

                                if (!pollResponse.IsSuccessStatusCode)
                                {
                                    if (this.debug)
                                        Console.WriteLine($"DEBUG: Poll returned {pollResponse.StatusCode}, retrying...");
                                    continue;
                                }

                                var pollContent = await pollResponse.Content.ReadAsStringAsync();
                                var pollPayload = JsonConvert.DeserializeObject<SSOTMEPayload>(pollContent);

                                if (string.Equals(pollPayload?.TaskStatus, "completed", StringComparison.OrdinalIgnoreCase) ||
                                    string.Equals(pollPayload?.TaskStatus, "failed", StringComparison.OrdinalIgnoreCase))
                                {
                                    responsePayload = pollPayload;
                                    break;
                                }

                                // Still pending
                                if (this.debug)
                                    Console.WriteLine($"DEBUG: Task still pending (elapsed: {stopwatch.ElapsedMilliseconds}ms)");
                            }
                            catch (TaskCanceledException)
                            {
                                continue;
                            }
                            catch (System.Net.Http.HttpRequestException ex)
                            {
                                if (this.debug)
                                    Console.WriteLine($"DEBUG: Poll error: {ex.Message}");
                                continue;
                            }
                        }

                        // If we exited the loop still pending, it's a timeout
                        if (string.Equals(responsePayload?.TaskStatus, "pending", StringComparison.OrdinalIgnoreCase))
                        {
                            this.result = new SSOTMEPayload
                            {
                                Exception = new Exception("Timed out waiting for async transpiler task to complete")
                            };
                            return;
                        }
                    }

                    // Fix missing transpiler name from remote transpiler
                    if (!string.IsNullOrEmpty(this.targetUrl) && this.transpiler.StartsWith("http"))
                    {
                        if (responsePayload?.Transpiler == null)
                        {
                            responsePayload.Transpiler = new Transpiler();
                        }

                        if (string.IsNullOrEmpty(responsePayload.Transpiler.Name))
                        {
                            // Remote transpiler didn't set a name, use our sanitized URL
                            responsePayload.Transpiler.Name = this.transpiler;
                            responsePayload.Transpiler.LowerHyphenName = SSoTmeProject.LowerHyphenName(this.transpiler);
                            if (this.debug)
                            {
                                Console.WriteLine($"DEBUG: Remote transpiler returned empty name, setting to: {responsePayload.Transpiler.LowerHyphenName}");
                            }
                        }
                    }

                    // Debug remote transpiler responses
                    if (!string.IsNullOrEmpty(this.targetUrl) && this.transpiler.StartsWith("http"))
                    {
                        var expectedName = this.transpiler;
                        var returnedName = responsePayload?.Transpiler?.Name ?? "NULL";
                        var hasZippedFileSet = responsePayload?.TranspileRequest?.ZippedOutputFileSet?.Length > 0;
                        if (!expectedName.StartsWith("http") && this.debug)
                        {
                            Console.WriteLine($"WARNING: Remote server {this.targetUrl} returned unexpected transpiler name.");
                            Console.WriteLine($"  Expected: '{expectedName}' (or similar remote transpiler name)");
                            Console.WriteLine($"  Received: '{returnedName}'");
                            Console.WriteLine($"  JSON Key: Response should set 'Transpiler.Name' to preserve the original name");
                            Console.WriteLine($"  This will cause the transpiler name to be reset during builds.");
                        }
                        if (!hasZippedFileSet && this.debug)
                        {
                            Console.WriteLine($"WARNING: Remote server {this.targetUrl} did not return expected output files.");
                            Console.WriteLine($"  JSON Key: Response should set 'TranspileRequest.ZippedOutputFileSet' with generated files");
                            Console.WriteLine($"  This will prevent files from being written to the project.");
                        }
                        else
                        {
                            // Validate ZFS content - check for files with no content
                            try
                            {
                                var zfsBytes = responsePayload?.TranspileRequest?.ZippedOutputFileSet;
                                if (zfsBytes?.Length > 0)
                                {
                                    var fileSetXml = zfsBytes.UnzipToString();
                                    if (!String.IsNullOrEmpty(fileSetXml) && fileSetXml.Contains("<"))
                                    {
                                        fileSetXml = fileSetXml.Substring(fileSetXml.IndexOf("<"));
                                        fileSetXml = fileSetXml.Replace("FileContents><?xml ", "FileContents>&lt;?xml");
                                        var doc = new System.Xml.XmlDocument();
                                        doc.LoadXml(fileSetXml);
                                        var fileSetNodes = doc.SelectNodes("//FileSetFile");
                                        var filesWithNoContent = new List<string>();

                                        foreach (System.Xml.XmlElement fileSetFileElem in fileSetNodes)
                                        {
                                            var relPathElem = fileSetFileElem.SelectSingleNode("RelativePath");
                                            if (relPathElem != null)
                                            {
                                                var fileContentsNode = fileSetFileElem.SelectSingleNode("FileContents");
                                                var zippedFileContents = fileSetFileElem.SelectSingleNode("ZippedTextFileContents");
                                                if (ReferenceEquals(zippedFileContents, null))
                                                {
                                                    zippedFileContents = fileSetFileElem.SelectSingleNode("ZippedFileContents");
                                                }
                                                var binaryFileContentsNode = fileSetFileElem.SelectSingleNode("BinaryFileContents");
                                                var zippedBinaryFileContentsNode = fileSetFileElem.SelectSingleNode("ZippedBinaryFileContents");
                                                bool hasContent = (!ReferenceEquals(fileContentsNode, null) && !String.IsNullOrEmpty(fileContentsNode.InnerXml)) ||
                                                                (!ReferenceEquals(zippedFileContents, null) && !String.IsNullOrEmpty(zippedFileContents.InnerXml)) ||
                                                                (!ReferenceEquals(binaryFileContentsNode, null) && !String.IsNullOrEmpty(binaryFileContentsNode.InnerText)) ||
                                                                (!ReferenceEquals(zippedBinaryFileContentsNode, null) && !String.IsNullOrEmpty(zippedBinaryFileContentsNode.InnerText));

                                                if (!hasContent)
                                                {
                                                    filesWithNoContent.Add(relPathElem.InnerText);
                                                }
                                            }
                                        }

                                        if (filesWithNoContent.Any() && this.debug)
                                        {
                                            Console.WriteLine($"WARNING: ZFS contains file entries with no content:");
                                            foreach (var file in filesWithNoContent)
                                            {
                                                Console.WriteLine($"  - {file}");
                                            }
                                            Console.WriteLine($"  Files without content cannot be properly cleaned during 'ssotme clean'");
                                            Console.WriteLine($"  This may indicate the transpiler is not generating complete file entries");
                                        }
                                    }
                                }
                            }
                            catch (Exception ex)
                            {
                                Console.WriteLine($"WARNING: Error validating ZFS content: {ex.Message}");
                            }
                        }
                    }

                    // Ensure LowerHyphenName is set correctly for remote transpilers after receiving response
                    if (!string.IsNullOrEmpty(this.targetUrl) && responsePayload?.Transpiler != null)
                    {
                        var sanitizedUrl = this.targetUrl.SanitizeUrlForFilename();
                        responsePayload.Transpiler.LowerHyphenName = sanitizedUrl;
                        if (this.debug)
                        {
                            Console.WriteLine($"DEBUG: Setting response transpiler .zfs name to SANITIZED URL: {sanitizedUrl}");
                        }
                    }

                    // Display logs if present in response (defensive handling for older transpilers)
                    try
                    {
                        // Try to access Logs property dynamically to handle transpilers without it
                        var logsProperty = responsePayload.GetType().GetProperty("Logs");
                        if (logsProperty != null)
                        {
                            var logs = logsProperty.GetValue(responsePayload);
                            if (logs != null)
                            {
                                var transpilerName = responsePayload.Transpiler?.Name ?? this.transpiler;
                                string firstErrorMessage = null;

                                // Try to enumerate logs (could be array or list)
                                if (logs is System.Collections.IEnumerable enumerable)
                                {
                                    foreach (var log in enumerable)
                                    {
                                        if (log != null && log is SassyMQ.SSOTME.Lib.RMQActors.LogEntry logEntry)
                                        {
                                            if (!this.suppressTranspilerErrorOutput)
                                            {
                                                DisplayLogEntry(logEntry, this.debug, transpilerName);
                                            }
                                            if (firstErrorMessage == null && string.Equals(logEntry.Level, "error", StringComparison.OrdinalIgnoreCase))
                                            {
                                                firstErrorMessage = logEntry.Text?.Trim();
                                            }
                                        }
                                    }
                                }

                                if (firstErrorMessage != null && ReferenceEquals(responsePayload.Exception, null) && !this.suppressTranspilerErrorOutput)
                                {
                                    responsePayload.Exception = new Exception(firstErrorMessage);
                                }
                            }
                        }
                    }
                    catch (Exception logEx)
                    {
                        // Silently ignore log display errors - transpiler might not support logs yet
                        if (this.debug)
                        {
                            Console.WriteLine($"[DEBUG] Could not display transpiler logs: {logEx.Message}");
                        }
                    }

                    // Preserve CLI debug flag from request to response
                    responsePayload.CLIDebug = this.debug;

                    // -------- QUOTA: post-transpile update --------
                    // MUST run BEFORE `this.result = responsePayload`. Setting `this.result`
                    // unblocks the main thread's waitForCook.Wait() and it immediately runs
                    // SaveFileSet, which reads Environment.CurrentDirectory. UpdateQuota calls
                    // InvokeToolAndGetOutput which mutates that process-global CWD to a temp
                    // dir. If we set result first, the main thread races in and writes files
                    // to the wrong location while this thread is still inside the temp-dir
                    // try block.
                    // If we sent quota context on the way in, look for quota-report.json in the
                    // returned FileSet, read the functionCount, and tell the bridge so it can
                    // apply the weight + update the account's current/peak usage.
                    if (quotaAttempted && !String.IsNullOrEmpty(quotaProjectUuid) && !String.IsNullOrEmpty(quotaTranspilerKey))
                    {
                        try
                        {
                            byte[] zippedOutput = responsePayload?.TranspileRequest?.ZippedOutputFileSet;
                            if (zippedOutput != null && zippedOutput.Length > 0)
                            {
                                var outputFsXml = zippedOutput.UnzipToString();
                                var outputFs = outputFsXml.ToFileSet();
                                var report = outputFs?.FileSetFiles?.FirstOrDefault(f =>
                                    f.RelativePath != null &&
                                    f.RelativePath.Trim('/', '\\').Equals("quota-report.json", StringComparison.OrdinalIgnoreCase));
                                if (report != null)
                                {
                                    var json = report.GetFileSetFileContents();
                                    if (!String.IsNullOrWhiteSpace(json))
                                    {
                                        var parsed = Newtonsoft.Json.Linq.JObject.Parse(json);
                                        var functionCountToken = parsed["functionCount"];
                                        if (functionCountToken != null && functionCountToken.Type != Newtonsoft.Json.Linq.JTokenType.Null)
                                        {
                                            var newNativeCount = functionCountToken.Value<decimal>();
                                            var updateResult = new SSoTme.OST.Lib.Services.MyEffortlessAPIService()
                                                .UpdateQuota(this.jwt, quotaProjectUuid, quotaTranspilerKey, newNativeCount);
                                            if (this.debug && updateResult != null)
                                                Console.WriteLine($"DEBUG: updateQuota {(updateResult.Success ? "ok" : "failed")}: delta={updateResult.Delta}, newUsed={updateResult.NewUsed}, newPeak={updateResult.NewPeak}, error={updateResult.Error}");

                                            // Surface a user-visible warning if the transpiler flagged this transpile as over-quota.
                                            var exceededToken = parsed["quotaExceeded"];
                                            if (exceededToken != null && exceededToken.Type == Newtonsoft.Json.Linq.JTokenType.Boolean && exceededToken.Value<bool>())
                                            {
                                                Console.ForegroundColor = ConsoleColor.Yellow;
                                                Console.WriteLine($"WARNING: This transpile exceeded your quota ({newNativeCount} units generated). Transpile succeeded; to avoid future overage charges please contact us to upgrade your plan: https://effortlessapi.com/rulebook/waitlist.");
                                                Console.ResetColor();
                                            }
                                        }
                                    }
                                }
                            }
                        }
                        catch (Exception ex)
                        {
                            if (this.debug) Console.WriteLine($"DEBUG: updateQuota post-processing threw: {ex.Message}");
                        }
                    }
                    // -------- end QUOTA: post-transpile update --------

                    // Set result LAST — this unblocks waitForCook.Wait() on the main thread.
                    // Any work that mutates process-global state (CWD, env vars) must run
                    // before this assignment to avoid a race with SaveFileSet on the main thread.
                    this.result = responsePayload;
                }
                catch (JsonException ex)
                {
                    this.result = new SSOTMEPayload
                    {
                        Exception = new Exception($"Failed to parse proxy response as JSON. Status: {response.StatusCode}, Content-Type: {response.Content.Headers.ContentType}, Response content: {responseContent}", ex)
                    };
                }
            }
            else
            {
                this.result = new SSOTMEPayload
                {
                    Exception = new Exception("No response received from proxy server")
                };
            }
        }

        private bool ShouldAutoBuildForInstall()
        {
            // Parse command line to check for -i flag, similar to ProjectTranspiler constructor logic
            var lowerCLI = Environment.CommandLine.ToLower().Replace("\\", "/");
            var cmd0 = Environment.CommandLine;

            // Remove common prefixes to get the actual transpiler command
            if (lowerCLI.Contains("/ssotme.exe")) cmd0 = cmd0.Substring(lowerCLI.IndexOf("/ssotme.exe") + "/ssotme.exe".Length);
            else if (lowerCLI.Contains("/ssotme.ost.cli.dll")) cmd0 = cmd0.Substring(lowerCLI.IndexOf("/ssotme.ost.cli.dll") + "/ssotme.ost.cli.dll".Length);
            else if (lowerCLI.Contains("/aicapture.ost.cli.dll")) cmd0 = cmd0.Substring(lowerCLI.IndexOf("/aicapture.ost.cli.dll") + "/aicapture.ost.cli.dll".Length);
            else if (lowerCLI.Contains("/ssotme")) cmd0 = cmd0.Substring(lowerCLI.IndexOf("/ssotme") + "/ssotme".Length);
            else if (lowerCLI.Contains("/aicapture")) cmd0 = cmd0.Substring(lowerCLI.IndexOf("/aicapture") + "/aicapture".Length);
            else if (lowerCLI.Contains("/aic")) cmd0 = cmd0.Substring(lowerCLI.IndexOf("/aic") + "/aic".Length);

            cmd0 = cmd0.Trim(" '\"".ToCharArray());

            // Remove install prefixes
            if (cmd0.StartsWith("/ssotme ")) cmd0 = cmd0.Substring("/ssotme ".Length);
            if (cmd0.StartsWith("install ")) cmd0 = cmd0.Substring("install ".Length);
            if (cmd0.StartsWith("-install ")) cmd0 = cmd0.Substring("-install ".Length);
            if (cmd0.StartsWith("ssotme ")) cmd0 = cmd0.Substring("ssotme ".Length);

            // Check if the cleaned command line contains -i flag
            var args = cmd0.Split(' ');
            var iIndex = Array.IndexOf(args, "-i");

            if (iIndex == -1 || iIndex + 1 >= args.Length)
            {
                // No -i flag or no filename after -i
                return false;
            }

            // Get the filename specified after -i
            var inputFileName = args[iIndex + 1];

            // Check if the input file already exists
            var inputFilePath = Path.Combine(Environment.CurrentDirectory, inputFileName);
            if (File.Exists(inputFilePath))
            {
                // Input file exists, no need to build
                return false;
            }

            // Input file doesn't exist and -i flag is present, should auto-build
            return true;
        }

