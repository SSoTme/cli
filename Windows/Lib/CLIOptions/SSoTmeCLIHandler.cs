        /// <summary>
        /// Runs the remote tools refresh: fetches the latest tool index from the server,
        /// persists update_available.json, and writes the cli_version marker.
        /// Called from CheckForUpdateNotice (on CLI version change) and TryGetUrlFromRemoteTools
        /// (when a tool is not found in the cached index).
        /// </summary>
        private void RunRemoteToolsRefresh(string remoteToolsDir, string cliVersionPath, string cachedCliVersion)
        {
            if (this.debug) Console.WriteLine($"DEBUG: running remote tools refresh (cached={cachedCliVersion ?? "none"}, current={this.CLI_VERSION})");
            _hasRunRemoteToolsUpdate = true;
            File.WriteAllText(cliVersionPath, this.CLI_VERSION);
            if (this.debug) Console.WriteLine($"DEBUG: wrote cli_version {this.CLI_VERSION} to {cliVersionPath}");

            // IMPORTANT: Any CWD changes in this method must be tightly scoped in try/finally blocks
            // that guarantee immediate restoration. This method is called from CheckForUpdateNotice
            // which must be purely presentational with zero lasting impact on CLI flow.

            // Use the stored bridge URL from tool_urls.json. Falls back to the bootstrap URL
            // only if nothing is stored yet. The bridge's list response includes latestBridgeVersion
            // which auto-updates tool_urls.json, so CLI version changes no longer force a URL reset.
            var effectiveUrl = TryGetUrlFromFileUrls(SSoTmeCLIHandler.TRANSPILERS_LISTER_TOOL_NAME) ?? SSoTmeCLIHandler.LATEST_TRANSPILERS_LISTER_URL;
            if (this.debug) Console.WriteLine($"DEBUG: using bridge url: {effectiveUrl}");

            // Write the URL to tool_urls.json so the transpiler handler can resolve it.
            this.SetToolUrl(SSoTmeCLIHandler.TRANSPILERS_LISTER_TOOL_NAME, effectiveUrl);
            // Remove legacy "list-transpilers" entry if present (renamed to cli-cloud-bridge)
            try { this.RemoveToolUrl("list-transpilers"); } catch (Exception ex) { if (this.debug) Console.WriteLine($"DEBUG: RemoveToolUrl('list-transpilers') skipped: {ex.Message}"); }
            if (this.debug) Console.WriteLine($"DEBUG: wrote {SSoTmeCLIHandler.TRANSPILERS_LISTER_TOOL_NAME}={effectiveUrl} to tool_urls.json");

            // Ensure a minimal ssotme.json exists in remoteToolsDir so the internal
            // handler has a valid project to load (avoids NullReferenceException).
            var remoteToolsSsotmeJson = Path.Combine(remoteToolsDir, "ssotme.json");
            if (!File.Exists(remoteToolsSsotmeJson))
            {
                var guid = Guid.NewGuid().ToString();
                File.WriteAllText(remoteToolsSsotmeJson, $@"{{
  ""ShowHidden"": false,
  ""ShowAllFiles"": false,
  ""CurrentPath"": null,
  ""SSoTmeProjectFiles"": null,
  ""Name"": ""remote_tools"",
  ""ProjectSettings"": [
    {{
      ""ProjectSettingId"": ""{guid}"",
      ""Name"": ""project-name"",
      ""Value"": ""remote_tools""
    }}
  ],
  ""ProjectTranspilers"": []
}}");
            }

            // Invoke the transpilers tool directly. The CWD must temporarily be set to remoteToolsDir
            // for ParseCommand/TranspileProject, but is tightly scoped in a try/finally to guarantee restoration.
            bool RunRefresh()
            {
                // Fail-fast DNS pre-check so a dead cached bridge URL doesn't burn 60s of retries
                // inside StartTranspile before the outer catch can swap it for the bootstrap URL.
                var currentUrl = TryGetUrlFromFileUrls(SSoTmeCLIHandler.TRANSPILERS_LISTER_TOOL_NAME) ?? SSoTmeCLIHandler.LATEST_TRANSPILERS_LISTER_URL;
                try
                {
                    var host = new Uri(currentUrl).Host;
                    System.Net.Dns.GetHostEntry(host);
                }
                catch (System.Net.Sockets.SocketException dnsEx) when (dnsEx.SocketErrorCode == System.Net.Sockets.SocketError.HostNotFound)
                {
                    throw new System.Net.Http.HttpRequestException($"No such host is known ({currentUrl})", dnsEx);
                }
                catch { /* non-DNS errors (e.g. malformed URI) — let the normal path handle it */ }

                var savedDir = Environment.CurrentDirectory;
                try
                {
                    Environment.CurrentDirectory = remoteToolsDir;
                    var transpilerHandler = CreateInternalHandler();
                    transpilerHandler.commandLine = $"{SSoTmeCLIHandler.TRANSPILERS_LISTER_TOOL_NAME} -p cli_version={this.CLI_VERSION}";
                    transpilerHandler.ParseCommand();
                    CliLog.SuppressFileLog = true;
                    try { transpilerHandler.TranspileProject(); }
                    finally { CliLog.SuppressFileLog = false; }
                }
                finally
                {
                    Environment.CurrentDirectory = savedDir;
                }

                // Check if the server flagged a CLI update and persist it
                var toolsJsonPath = Path.Combine(remoteToolsDir, "ssotme-tools.json");
                var updateAvailablePath = Path.Combine(SSOTMEKey.SSoTmeDir.FullName, "update_available.json");
                if (File.Exists(toolsJsonPath))
                {
                    try
                    {
                        var root = Newtonsoft.Json.Linq.JObject.Parse(File.ReadAllText(toolsJsonPath));
                        var updateAvailable = root["cliUpdateAvailable"];
                        if (updateAvailable != null && updateAvailable.Type != Newtonsoft.Json.Linq.JTokenType.Null && !String.IsNullOrEmpty(updateAvailable["name"]?.Value<string>()))
                            File.WriteAllText(updateAvailablePath, updateAvailable.ToString());
                        else if (File.Exists(updateAvailablePath))
                            File.Delete(updateAvailablePath);

                        // Auto-discover newer bridge versions. The bridge includes
                        // latestBridgeVersion: { name, version, url, versionIndex }
                        // in the list response. If its versionIndex is higher than what
                        // we have stored, update tool_urls.json so the next CLI invocation
                        // uses the new bridge automatically.
                        var bridgeInfo = root["latestBridgeVersion"];
                        if (bridgeInfo != null && bridgeInfo.Type == Newtonsoft.Json.Linq.JTokenType.Object)
                        {
                            var newUrl = bridgeInfo["url"]?.Value<string>();
                            var newIdx = bridgeInfo["versionIndex"]?.Value<long>() ?? -1;
                            if (!String.IsNullOrEmpty(newUrl) && newIdx >= 0)
                            {
                                // Read the stored bridge version index (persisted alongside tool_urls.json)
                                var bridgeIdxPath = Path.Combine(SSOTMEKey.SSoTmeDir.FullName, "bridge_version_index");
                                long storedIdx = -1;
                                if (File.Exists(bridgeIdxPath))
                                    long.TryParse(File.ReadAllText(bridgeIdxPath).Trim(), out storedIdx);

                                if (newIdx > storedIdx)
                                {
                                    // Ensure URL ends with /
                                    if (!newUrl.EndsWith("/")) newUrl += "/";
                                    this.SetToolUrl(SSoTmeCLIHandler.TRANSPILERS_LISTER_TOOL_NAME, newUrl);
                                    File.WriteAllText(bridgeIdxPath, newIdx.ToString());
                                    if (this.debug) Console.WriteLine($"DEBUG: Bridge auto-updated to {bridgeInfo["version"]?.Value<string>()} (idx {newIdx}): {newUrl}");
                                }
                            }
                        }
                    }
                    catch { /* best-effort */ }
                }
                return true;
            }

            try
            {
                RunRefresh();
            }
            catch (Exception refreshEx) when (refreshEx.InnerException is System.Net.Sockets.SocketException se && se.SocketErrorCode == System.Net.Sockets.SocketError.HostNotFound
                                               || refreshEx.Message.Contains("No such host"))
            {
                // The stored bridge URL in tool_urls.json points to a dead host.
                // Reset it to the bootstrap URL and retry once so the user isn't stuck
                // with a permanently broken cache.
                if (effectiveUrl != SSoTmeCLIHandler.LATEST_TRANSPILERS_LISTER_URL)
                {
                    Console.WriteLine($"Stored bridge URL is unreachable ({effectiveUrl}). Resetting to bootstrap URL and retrying...");
                    this.SetToolUrl(SSoTmeCLIHandler.TRANSPILERS_LISTER_TOOL_NAME, SSoTmeCLIHandler.LATEST_TRANSPILERS_LISTER_URL);
                    var bridgeIdxPath = Path.Combine(SSOTMEKey.SSoTmeDir.FullName, "bridge_version_index");
                    try { if (File.Exists(bridgeIdxPath)) File.Delete(bridgeIdxPath); } catch { }
                    try
                    {
                        RunRefresh();
                    }
                    catch (Exception retryEx)
                    {
                        Console.WriteLine($"Warning: Could not refresh remote tool list after reset ({retryEx.Message}). Continuing...");
                        if (this.debug) Console.WriteLine($"DEBUG: retry exception: {retryEx}");
                    }
                }
                else
                {
                    Console.WriteLine($"Warning: Could not refresh remote tool list (host not found: {effectiveUrl}). Continuing...");
                    if (this.debug) Console.WriteLine($"DEBUG: refresh exception: {refreshEx}");
                }
            }
            catch (Exception refreshEx)
            {
                Console.WriteLine($"Warning: Could not refresh remote tool list ({refreshEx.Message}). Falling back to RabbitMQ proxy.");
                if (this.debug) Console.WriteLine($"DEBUG: refresh exception: {refreshEx}");
            }
        }

        private void CheckForUpdateNotice()
        {
            if (Environment.GetEnvironmentVariable("SSOTME_CHILD_PROCESS") == "1") return;
            try
            {
                // If the CLI version has changed since the last remote tools refresh,
                // force a refresh so that update_available.json reflects the current state.
                var remoteToolsDir = GetRemoteToolsDir();
                var cliVersionPath = Path.Combine(remoteToolsDir, "cli_version");
                var cachedCliVersion = File.Exists(cliVersionPath) ? File.ReadAllText(cliVersionPath).Trim() : null;
                if (cachedCliVersion != this.CLI_VERSION && Directory.Exists(remoteToolsDir))
                {
                    EnsureRemoteToolsInitialized();
                    RunRemoteToolsRefresh(remoteToolsDir, cliVersionPath, cachedCliVersion);
                }

                var updateAvailablePath = Path.Combine(SSOTMEKey.SSoTmeDir.FullName, "update_available.json");
                if (!File.Exists(updateAvailablePath)) return;
                var root = Newtonsoft.Json.Linq.JObject.Parse(File.ReadAllText(updateAvailablePath));
                var versionName = root["name"]?.Value<string>();
                var links = root["installLinks"];
                var installLink = RuntimeInformation.IsOSPlatform(OSPlatform.OSX)
                    ? (RuntimeInformation.ProcessArchitecture == Architecture.Arm64
                        ? links?["macArm"]?.Value<string>()
                        : links?["mac"]?.Value<string>())
                    : (RuntimeInformation.ProcessArchitecture == Architecture.Arm64
                        ? links?["windowsArm"]?.Value<string>()
                        : links?["windows"]?.Value<string>());
                var advertisedVersion = ParseVersionKey(versionName ?? "", false);
                var currentVersion = ParseVersionKey(this.CLI_VERSION, false);
                if (advertisedVersion.CompareTo(currentVersion) > 0 && !String.IsNullOrEmpty(installLink))
                    CliLog.LogLine($"A new version of ssotme is available: {versionName}  Download: {installLink}");
                else
                    File.Delete(updateAvailablePath);
            }
            catch { /* best-effort */ }

            CheckForGitHubUpdate();
        }

